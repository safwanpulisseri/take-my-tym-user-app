/// Default radius values used throughout the application.
///
/// This class provides static constants representing various radius values
/// that can be used for styling UI components. It cannot be instantiated.
final class AppRadius {
  // Private constructor to prevent instantiation.
  AppRadius._();

  /// Represents a standard border radius.
  static const double borderRadius = 10.0;

  /// Represents a fully rounded border radius.
  static const double borderRound = 50.0;
}
