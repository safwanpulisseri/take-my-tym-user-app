/// Default padding values used throughout the application.
///
/// This class provides static constants representing various padding values
/// that can be used for consistent spacing in different parts of the application. It cannot be instantiated.
final class MyAppPadding {
  // Private constructor to prevent instantiation.
  MyAppPadding._();

  /// Represents the default padding value used in authentication screens.
  static const double authPadding = 20.0;

  /// Represents the default padding value used in home screens.
  static const double homePadding = 10.0;
}
